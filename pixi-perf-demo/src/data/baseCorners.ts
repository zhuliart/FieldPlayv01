// 设计舞台尺寸（任务书：固定 1280×720 设计舞台 + 等比缩放）
export const STAGE_W = 1280;
export const STAGE_H = 720;

// 把「% of 背景」坐标换算成舞台像素
export function pctToStage(c: Corner): Corner {
  return [(c[0] / 100) * STAGE_W, (c[1] / 100) * STAGE_H];
}
