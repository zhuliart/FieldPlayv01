// (x%,y%) 处背景是否已有树/灌木
export function isBgVeg(xPct: number, yPct: number): boolean {
  const cx = Math.max(0, Math.min(VEG_GW - 1, Math.floor((xPct / 100) * VEG_GW)));
  const cy = Math.max(0, Math.min(VEG_GH - 1, Math.floor((yPct / 100) * VEG_GH)));
  return ROWS[cy].charCodeAt(cx) === 49; // '1'
}
